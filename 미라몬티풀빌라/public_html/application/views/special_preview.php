<script>


 if ($(window).width() > 768) {
$('.Listcontainer a').hover(function(){
  var index = $(this).closest('.Listcontainer').index(); // 현재 요소의 부모 .Listcontainer의 인덱스 가져오기
  $(this).find('.more').hide();
  $(this).find('.more_hover').show();
},function(){
  $(this).find('.more').show();
  $(this).find('.more_hover').hide();
});
 };

window.addEventListener("scroll", function() {
    var sideMenu = document.querySelector(".side_menu");
    var spList = document.querySelector(".spList");
    var triggerOffset = spList.offsetTop; // 트리거 지점 설정

    if (window.pageYOffset >= triggerOffset) {
        sideMenu.classList.add("sticky");
        sideMenu.style.display = "block"; // 화면에 표시되도록 함
    }  else {
            sideMenu.style.display = 'none'; // 그렇지 않으면 사이드 메뉴를 숨김
        }
    
});


window.addEventListener("scroll", function() {
    var listContainers = document.querySelectorAll('.Listcontainer');
    var sideMenuLinks = document.querySelectorAll('.side_menu ul li');

    // 현재 스크롤 위치를 계산하여 보여지는 Listcontainer의 인덱스를 찾음
    var visibleIndex = -1;
    for (var i = 0; i < listContainers.length; i++) {
        var container = listContainers[i];
        var topOffset = container.offsetTop;
        var bottomOffset = topOffset + container.offsetHeight + 150;

        // 현재 스크롤 위치가 해당 컨테이너 범위 내에 있는지 확인
        if (window.pageYOffset >= topOffset && window.pageYOffset < bottomOffset) {
            visibleIndex = i;
            break;
        }
    }

    // 모든 side menu 링크의 배경색 초기화
    sideMenuLinks.forEach(function(link) {
        link.style.backgroundColor = ''; // 기본 배경색으로 초기화
    });

    // 현재 보여지는 Listcontainer에 대응하는 side menu 링크의 배경색 변경
    if (visibleIndex !== -1) {
    // 뷰포트 폭이 768px 이하이고, 다음 인덱스가 존재하면서 현재 인덱스의 배경색이 변경되지 않았을 때, 다음 인덱스의 배경색 변경
    if (window.innerWidth <= 768 && visibleIndex < sideMenuLinks.length - 1 && sideMenuLinks[visibleIndex + 1].style.backgroundColor !== '#E3BB17') {
        sideMenuLinks[visibleIndex + 1].style.backgroundColor = '#E3BB17'; // 다음 인덱스의 배경색 변경
    }
    else {
        sideMenuLinks[visibleIndex].style.backgroundColor = '#E3BB17'; // 현재 인덱스의 배경색 변경
    }
}
});


document.addEventListener('DOMContentLoaded', function() {
  // side_menu 클래스 안에 있는 모든 a 태그를 선택
  var links = document.querySelectorAll('.side_menu a');

  // 각 링크에 대해 클릭 이벤트를 추가합니다.
  links.forEach(function(link) {
    link.addEventListener('click', function(event) {
      // 이벤트의 기본 동작인 링크 이동
      event.preventDefault();

      // 링크의 href 속성 값을 가져옴
      var href = link.getAttribute('href');

      // 현재 뷰포트의 너비를 가져옴
      var viewportWidth = window.innerWidth || document.documentElement.clientWidth;

      // 분기하여 링크를 선택
      var selectedLink;
      if (viewportWidth < 768) {
        // 화면 너비가 768px 미만인 경우 #link11을 선택
        selectedLink = href.split(' ')[1];
      } else {
        // 화면 너비가 768px 이상인 경우 #link1을 선택
        selectedLink = href.split(' ')[0];
      }

      // 선택된 링크로 이동
      window.location.href = selectedLink;
    });
  });
});
</script>
